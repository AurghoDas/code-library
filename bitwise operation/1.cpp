#include<bits/stdc++.h>
using namespace std;

int main() {
    int a = 4;//4 = 100
    int b = 6;//6 = 110

    cout << "a&b = " << (a&b) << endl;
    cout << "a|b = " << (a|b) << endl;
    cout << "~a = " << (~a) << endl;
    cout << "a^b = " << (a^b) << endl;

    cout << (17>>1) << endl;
    cout << (17>>2) << endl;
    cout << (19<<1) << endl;
    cout << (21<<2) << endl;
} 
